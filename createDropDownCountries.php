function createDropDown(array $country_array): string
{

    if (!is_array($country_array) || count($country_array) === 0) {
        return '<option>Invalid or empty country list provided.</option>';
    }

    $options = '';

    // Generate options
    foreach ($country_array as $name) {
        $escaped_name = htmlspecialchars($name, ENT_QUOTES, 'UTF-8');
        $options .= "<option value='$escaped_name'>$escaped_name</option>";
    }

    return $options;
}